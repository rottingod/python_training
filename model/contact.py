class Contact:

    def __init__(self, name, middle_name, last_name, nickname, company, address, home, mobile, email):
        self.name = name
        self.middle_name = middle_name
        self.last_name = last_name
        self.nickname = nickname
        self.company = company
        self.address = address
        self.home = home
        self.mobile = mobile
        self.email = email
